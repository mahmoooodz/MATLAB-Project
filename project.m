ylim([min(f1)-0.2 max(f1)+0.2])
grid on 

% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, ~, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
fs=str2double(get(handles.fs,'string'));
h=inline(get(handles.s,'string'));
T=eval(get(handles.T,'string'));
ts=1/fs;
t=-1+T(1):ts:T(length(T))+1-ts;
h1=fft(h(T));
h1=h1/length(h1);
f=-fs/2:fs/length(h1):fs/2-fs/length(h1);

figure(1)
plot(f,fftshift(abs(h1)));



% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

figure(2)
f=inline(get(handles.s,'string'));
T = eval(get(handles.T,'String'));
t=-2:0.01:2;
h=inline('(t>-1)-(t>1)'); 
y = conv(f(T),h(t))*0.01;
tn=(t(1)+T(1)):0.01:(t(length(t))+T(length(T)));

subplot(3,1,1)
plot(T,f(T))
ylim ([ min(f(T))-0.5 max(f(T))+0.5]) 
grid on 
xlabel('t')
ylabel('f(t)')
subplot(3,1,2)
plot(t,h(t))
ylim ([ min(h(t))-0.5 max(h(t))+0.5])
grid on 
xlabel('t')
ylabel('rect(t/2)')
subplot(3,1,3)
plot(tn,y)
ylim ([ min(y)-0.5 max(y)+0.5])
grid on 
xlabel('t')
ylabel('y(t)')


